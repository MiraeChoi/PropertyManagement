package vo;

public class GonginVO {
	private int idx;
	private String name;
	private String id;
	private String pwd;
	private String gong_name;
	private String gong_reg;
	private String tel;
	private String email;
	
	public int getIdx() {
		return idx;
	}
	public void setIdx(int idx) {
		this.idx = idx;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getPwd() {
		return pwd;
	}
	public void setPwd(String pwd) {
		this.pwd = pwd;
	}
	public String getGong_name() {
		return gong_name;
	}
	public void setGong_name(String gong_name) {
		this.gong_name = gong_name;
	}
	public String getGong_reg() {
		return gong_reg;
	}
	public void setGong_reg(String gong_reg) {
		this.gong_reg = gong_reg;
	}
	public String getTel() {
		return tel;
	}
	public void setTel(String tel) {
		this.tel = tel;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
}
